package com.syntex.class02;

public class ArithemeticOperators {

	public static void main(String[] args) {
		//Arithmetic Operators: +,-,*,/,%,
		int num1, num2;
		num1=10;
		num2=12;
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		
		double num3=10.99;
		double num4=2.99;
		
		double mult=num3*num4;
		System.out.println(mult);
		
		double div=num3/num4;
		//mult/2
		System.out.println("The result of 2 double values= "+div);
		
		float f=10.99f;
		float f1=2.99f;
		
		System.out.println("The result of 2 flost values = "+f/f1);
		
		
		
		
		
	}

}
